// Operadores relacionais

// Usando valores literais
console.log(10 > 5); // true
console.log(10 < 5); // false
console.log(10 >= 5);
console.log(10 <= 5);
console.log(10 == 5);
console.log(10 != 5);
console.log(10 == '10'); // comparação implícita
console.log(10 === '10'); // comparação estrita

// Usando variáveis/constantes
let x = 10;
const y = 5;

console.log(x > y);
console.log(x < y);
console.log(x >= y);
console.log(x <= y);
console.log(x == y);
console.log(x != y);
console.log(x == '10');
console.log(x === '10');
console.log(x !== '10');
